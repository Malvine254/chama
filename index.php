        // Background Slideshow Functionality
        const slides = document.querySelectorAll('.slide');
        let currentSlide = 0;
        
        function nextSlide() {
            slides[currentSlide].classList.remove('active');
            currentSlide = (currentSlide + 1) % slides.length;
            slides[currentSlide].classList.add('active');
        }
        
        // Change slide every 5 seconds
        setInterval(nextSlide, 5000);
        
        // Community Slideshow Functionality
        const communitySlides = document.querySelectorAll('.community-slide');
        const dots = document.querySelectorAll('.slide-dot');
        let currentCommunitySlide = 0;
        
        function showCommunitySlide(index) {
            communitySlides.forEach(slide => slide.classList.remove('active'));
            dots.forEach(dot => dot.classList.remove('active'));
            
            communitySlides[index].classList.add('active');
            dots[index].classList.add('active');
            currentCommunitySlide = index;
        }
        
        function nextCommunitySlide() {
            let nextIndex = (currentCommunitySlide + 1) % communitySlides.length;
            showCommunitySlide(nextIndex);
        }
        
        // Change community slide every 4 seconds
        setInterval(nextCommunitySlide, 4000);
        
        // Dot navigation for community slides
        dots.forEach(dot => {
            dot.addEventListener('click', function() {
                const index = parseInt(this.getAttribute('data-index'));
                showCommunitySlide(index);
            });
        });
        
        // Simple animation for elements on scroll
        document.addEventListener('DOMContentLoaded', function() {
            const animateElements = document.querySelectorAll('.service-card, .gallery-item, .product-card, .donation-card');
            
            window.addEventListener('scroll', function() {
                animateElements.forEach(element => {
                    const elementPosition = element.getBoundingClientRect().top;
                    const screenPosition = window.innerHeight / 1.3;
                    
                    if(elementPosition < screenPosition) {
                        element.style.opacity = '1';
                        element.style.transform = 'translateY(0)';
                    }
                });
            });
            
            // Initialize element positions
            animateElements.forEach(element => {
                element.style.opacity = '0';
                element.style.transform = 'translateY(20px)';
                element.style.transition = 'all 0.5s ease';
            });
            
            // Trigger scroll event to check initial positions
            window.dispatchEvent(new Event('scroll'));
        });
    </script>
